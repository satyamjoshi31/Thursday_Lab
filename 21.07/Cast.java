class Cast
{
	public static void main(String []a)
	{
		int i = 81;
		char i2 = (char)i;
		double dou = 323.142d;
		int dou2 = (int) dou;
		float fl = 72.38f;
		int fl2 = (int) fl;
		byte dou3 = (byte) dou;

		System.out.println("char value of 81 = "+ i2);
		System.out.println("integer value of 323.142 = " + dou2);
		System.out.println("integer value of 72.38 = " + fl2);
		System.out.println("byte value of 323.142 = " + dou3 ); 	
	}
}